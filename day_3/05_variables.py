# variables
x = 5
y = 10
print("value of x is", x)  # 5
print("value of y is", y)  # 10
str = "Hello, World!"
print(str)  # prints "Hello, World!"

print(type(x))  # int
print(type(y))  # int
print(type(str))  # string

# Rules for variable names
# 1. A variable name must start with a letter or an underscore (_).
# 2. A variable name cannot start with a number.
# 3. A variable name can only contain letters, numbers, and underscores.
# 4. A variable name cannot contain any spaces or special characters.
# 5. A variable name cannot be a reserved keyword in Python.
# 6. A variable name should be descriptive and meaningful.
# 7. A variable name should be in lowercase with words separated by underscores.
# 8. A variable name should not be too long.
# 9. A variable name should not be too short.
# 10. A variable name should be consistent throughout the code
# 11. A variable name should not be a function name
# 12. A variable name should not be a class name

